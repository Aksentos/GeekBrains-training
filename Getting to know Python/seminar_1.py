"""Задача № 4
Определите, можно ли от шоколадки размером a × b долек отломить c долек, если 
разрешается сделать один разлом по прямой между дольками 
(то есть разломить шоколадку на два прямоугольника).
Выведите yes или no соответственно.
"""
# 1 вариант
a = int(input("Введите число первой стороны шоколадки: "))
b = int(input("Введите число второй стороны шоколадки: "))
c = int(input("Введите число долек: "))
res = "no"
if c < a * b:
    for i in range(b):
        if c == (i + 1) * a:
            res = "yes"
    for i in range(a):
        if c == (i + 1) * b:
            res = "yes"
print(res)

# 2 варинат
print("yes" if a * b > c and (c % a == 0 or c % b == 0) else "no")

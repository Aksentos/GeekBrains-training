"""Задача № 1
Сумма цифр трехзначного числа
Найдите сумму цифр трехзначного числа n.
Результат сохраните в перменную res."""
# Вариант 1
n = int(input("Введите число: "))
res = 0
while n > 0: # проходим циклом пока число n > 0
    x = n % 10 # берем последнюю цифру числа n
    res += x # добавляем в результат
    n = n // 10 # меняем число n на число без последней цифры
print(res)

